function actualizar(id_estacion){
 $.ajax({
        url: '/dosisEstacion/'+id_estacion,
        dataType: 'text',
        success: function(data) {
           $('#datos_estacion').html(data);
           tasa_dosis=document.getElementById("tasa_dosis");
           datos_tasa_dosis=document.getElementById("datos_estacion").innerHTML;
          datos_tasa_dosis=eval(datos_tasa_dosis);
           new Morris.Line({
                      element: tasa_dosis,
                      data: datos_tasa_dosis,
                      xkey: 'date',
                      ykeys: ['value'],
                      labels: ['Value'],
                      pointSize:0,
                      resize:true
                      
                  });


        }
    });
}
